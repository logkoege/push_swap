#include "push_swap.h"

int	caca_parsing(char *argv)
{
	int	i;
	
	i = i;
	if (argc > 2)
	{
		while (i != argc)
		{
			if (ft_atoi(argv[i]) <= -2147483648)
				return (0);
			i++;
		}
	}
	
}